package Practica1;

public class Millaje {
	private String destino;
	private String categoria;
	private double millasAcreditar;
	
	public String getDestino() {
		return destino;
	}
	public void setDestino(String destino) {
		this.destino = destino;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public double getMillasAcreditar() {
		return millasAcreditar;
	}
	public void setMillasAcreditar(double millasAcreditar) {
		this.millasAcreditar = millasAcreditar;
	}
}
